		System.out.println("(int)fabove = " + (int)fabove + "  " + "(int)fbelow  = " + (int)fbelow);
		System.out.println("(Math.roungd)above = " + Math.round(above) + "  " + "(Math.roungd)below  = " + Math.round(below));
